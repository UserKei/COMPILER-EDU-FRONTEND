<template>
  <div class="lr-canvas">
    <!-- 工具栏 -->
    <div class="toolbar">
      <h3 class="text-lg font-semibold text-gray-800">{{ displayTitle }} 编辑器</h3>
      <div class="controls">
        <button
          v-for="button in toolbarButtons"
          :key="button.label"
          @click="button.action"
          :disabled="button.disabled"
          class="px-3 py-1 rounded transition-colors text-white"
          :class="getButtonClasses(button.variant)"
        >
          {{ button.label }}
        </button>
      </div>
    </div>

    <!-- 画布区域 -->
    <div class="canvas-container">
      <VueFlow
        :nodes="canvasNodes"
        :edges="canvasEdges"
        :node-types="nodeTypes"
        :edge-types="edgeTypes"
        :default-viewport="defaultViewport"
        :min-zoom="canvasConfig.minZoom"
        :max-zoom="canvasConfig.maxZoom"
        :connect-on-click="false"
        :nodes-connectable="true"
        :nodes-draggable="true"
        :selection-on-drag="false"
        :pan-on-drag="[2]"
        :zoom-on-double-click="false"
        fit-view-on-init
        class="vue-flow lr-mode"
        @connect="onConnect"
        @node-click="onNodeClick"
        @edge-click="onEdgeClick"
        @pane-click="onPaneClick"
        @pane-double-click="onPaneDoubleClick"
        @pane-context-menu="onPaneContextMenu"
        @pane-ready="onPaneReady"
      >
        <!-- 控制器 -->
        <Controls v-if="canvasConfig.showControls" position="top-right" />

        <!-- 背景 -->
        <Background
          v-if="canvasConfig.showBackground"
          :pattern="canvasConfig.backgroundPattern"
          :gap="canvasConfig.backgroundGap"
        />

        <!-- 自定义标记 -->
        <svg>
          <defs>
            <marker
              id="lr-arrow"
              viewBox="0 0 10 10"
              refX="9"
              refY="5"
              markerWidth="6"
              markerHeight="6"
              orient="auto"
              markerUnits="strokeWidth"
            >
              <path d="M 0 0 L 10 5 L 0 10 z" :fill="themeColor" />
            </marker>
          </defs>
        </svg>
      </VueFlow>
    </div>

    <!-- 提示信息 -->
    <div class="help-text">
      <p class="text-sm text-gray-600">
        💡 提示：双击画布空白处创建项目集，点击节点可编辑LR项目内容，选择两个节点后可连接
      </p>
    </div>

    <!-- 统计信息 -->
    <div v-if="showStats" class="stats-panel">
      <div class="bg-gray-50 border border-gray-200 rounded-lg p-3 mt-2">
        <h4 class="text-sm font-medium text-gray-800 mb-2">统计信息：</h4>
        <div class="grid grid-cols-2 gap-4 text-xs text-gray-600">
          <div>项目集数量: {{ canvasNodes.length }}</div>
          <div>转换数量: {{ canvasEdges.length }}</div>
          <div>平均项目数: {{ averageItemsPerSet }}</div>
          <div>最大项目数: {{ maxItemsPerSet }}</div>
        </div>
      </div>
    </div>
  </div>
</template>

<script setup lang="ts">
import { ref, computed, onMounted, markRaw } from 'vue'
import { VueFlow, type Node, type Edge, type Connection } from '@vue-flow/core'
import { Controls } from '@vue-flow/controls'
import { Background } from '@vue-flow/background'
import RectangleNode from '../components/rectangleNode/index.vue'
import CustomEdge from '../components/edges/index.vue'
import { useNodeCreation, useCanvasEvents, useEdgeManagement } from '../composables'
import { useFlowCanvasStore, useFlowEditorStore } from '../stores'
import { ColorUtils } from '../utils'
import type { CanvasMode, ToolbarButton, LRItem } from '../types'

// 定义 props
interface Props {
  mode?: CanvasMode
  title?: string
  showStats?: boolean
}

const props = withDefaults(defineProps<Props>(), {
  mode: 'lr0',
  title: '',
  showStats: true,
})

// 使用 stores
const canvasStore = useFlowCanvasStore()
const editorStore = useFlowEditorStore()

// 初始化模式
onMounted(() => {
  canvasStore.setMode(props.mode)
})

// 计算属性
const displayTitle = computed(() => {
  if (props.title) return props.title
  const modeMap: Record<string, string> = {
    lr0: 'LR(0)',
    slr1: 'SLR(1)',
    ll1: 'LL(1)',
  }
  return modeMap[props.mode] || 'LR 项目集'
})

const themeColor = computed(() => ColorUtils.getThemeColor(props.mode))

const canvasConfig = computed(() => canvasStore.canvasConfig)
const canvasNodes = computed(() => canvasStore.nodes)
const canvasEdges = computed(() => canvasStore.edges)

const defaultViewport = computed(() => ({
  zoom: canvasConfig.value.defaultZoom || 1,
}))

// 统计信息
const averageItemsPerSet = computed(() => {
  if (canvasNodes.value.length === 0) return 0
  const totalItems = canvasNodes.value.reduce((sum, node) => {
    const items = (node.data?.items || node.data?.pros || []) as LRItem[]
    return sum + items.length
  }, 0)
  return Math.round((totalItems / canvasNodes.value.length) * 10) / 10
})

const maxItemsPerSet = computed(() => {
  return canvasNodes.value.reduce((max, node) => {
    const items = (node.data?.items || node.data?.pros || []) as LRItem[]
    return Math.max(max, items.length)
  }, 0)
})

// 定义节点和边类型
const nodeTypes = {
  rectangle: markRaw(RectangleNode),
}

const edgeTypes = {
  custom: markRaw(CustomEdge),
}

// 使用组合函数
const { handlePaneDoubleClick, clearAll } = useNodeCreation(canvasNodes, canvasEdges, {
  nodeType: 'rectangle',
})

const {
  onConnect: handleConnect,
  deleteSelected,
  clearCanvas,
} = useCanvasEvents(canvasNodes, canvasEdges, ref(props.mode))

const { createEdge } = useEdgeManagement(canvasEdges)

// 工具栏按钮配置
const toolbarButtons = computed((): ToolbarButton[] => [
  {
    label: '添加项目集',
    action: () => addItemSet(),
    variant: 'primary',
  },
  {
    label: '连接项目集',
    action: () => connectItemSets(),
    disabled: editorStore.selectedNodes.length !== 2,
    variant: 'secondary',
  },
  {
    label: '清空画布',
    action: () => clearCanvas(),
    variant: 'danger',
  },
  {
    label: '删除选中',
    action: () => deleteSelected(),
    disabled: !editorStore.hasSelection,
    variant: 'danger',
  },
])

// 工具栏按钮样式
const getButtonClasses = (variant: string = 'primary') => {
  const baseClasses = 'px-3 py-1 rounded transition-colors text-white'
  const variantClasses: Record<string, string> = {
    primary: 'bg-blue-500 hover:bg-blue-600 disabled:bg-gray-400',
    secondary: 'bg-green-500 hover:bg-green-600 disabled:bg-gray-400',
    danger: 'bg-red-500 hover:bg-red-600 disabled:bg-gray-400',
  }
  return `${baseClasses} ${variantClasses[variant] || variantClasses.primary}`
}

// 事件处理函数
const onConnect = (connection: Connection) => {
  const success = handleConnect(connection, {
    markerEnd: 'url(#lr-arrow)',
  })

  if (success) {
    editorStore.saveToHistory(canvasNodes.value, canvasEdges.value)
  }
}

const onNodeClick = (nodeMouseEvent: any) => {
  const { event, node } = nodeMouseEvent
  editorStore.selectNode(node.id, event.ctrlKey || event.metaKey)
}

const onEdgeClick = (edgeMouseEvent: any) => {
  const { event, edge } = edgeMouseEvent
  editorStore.selectEdge(edge.id, event.ctrlKey || event.metaKey)
}

const onPaneClick = () => {
  editorStore.clearSelection()
}

const onPaneContextMenu = (event: MouseEvent) => {
  event.preventDefault()
  // 可以实现右键菜单
}

const onPaneReady = () => {
  console.log('LR Canvas ready')
}

const onPaneDoubleClick = (event: MouseEvent) => {
  handlePaneDoubleClick(event)
  editorStore.saveToHistory(canvasNodes.value, canvasEdges.value)
}

// 工具栏操作函数
const addItemSet = () => {
  // 在画布中心添加新的项目集
  const canvasCenter = { x: 400, y: 300 }

  const { createNodeAtPosition } = useNodeCreation(canvasNodes, canvasEdges, {
    nodeType: 'rectangle',
  })

  createNodeAtPosition(canvasCenter)
  editorStore.saveToHistory(canvasNodes.value, canvasEdges.value)
}

const connectItemSets = () => {
  const selectedNodeIds = editorStore.selectedNodes
  if (selectedNodeIds.length !== 2) return

  const connection = {
    source: selectedNodeIds[0],
    target: selectedNodeIds[1],
    sourceHandle: 'center-source',
    targetHandle: 'center-target',
  }

  createEdge(connection, {
    markerEnd: 'url(#lr-arrow)',
    isEditing: true,
  })

  editorStore.saveToHistory(canvasNodes.value, canvasEdges.value)
}
</script>

